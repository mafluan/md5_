import streamlit as st
import hashlib

st.title("MD5 Хешировщик")
st.write("Введите текст, и программа покажет его MD5-хеш и объяснит, как это работает.")

user_input = st.text_input("Введите текст")

if st.button("Сгенерировать MD5"):
    if user_input:
        md5_hash = hashlib.md5(user_input.encode()).hexdigest()
        st.write(f"*MD5-хеш:* {md5_hash}")

        st.markdown("### Объяснение результата:")
        st.write("MD5 — это криптографическая хеш-функция, которая берёт входной текст и превращает его в строку длиной 32 символа.")
        st.write("Алгоритм работает так:")
        st.write("""
        - Преобразует текст в байты
        - Делит на блоки по 512 бит
        - Применяет серию битовых операций и смешивания данных
        - Результат — уникальный 'отпечаток' текста
        """)
        st.write("Даже небольшое изменение во входе даст совсем другой хеш.")
    else:
        st.warning("Пожалуйста, введите текст.")